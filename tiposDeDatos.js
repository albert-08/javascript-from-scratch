/**
 * Tipos de datos
 */

// Number - Para todos los tipos de números. Enteros y decimales

console.log(typeof 50);
console.log(typeof 50.7);

// String - Cadena de texto

console.log(typeof 'Beto Flores')

console.log(typeof 'a')
console.log(typeof ' ');

// Boolean - false, true

console.log(typeof true)
console.log(typeof false)

// Colección de datos

/**
 * Objetos:
 * Colecciones de datos (información)
 * que tienen un identificador y un valor
 */

// Array (Arreglo)
console.log(["Jose", "José", "Juan", "Jean", "Oto"])
console.log(typeof ["Jose", "José", "Juan", "Jean", "Oto"]);

// Objetos
console.log({
  nombre: "Beto",
  apellido: "Flores"
})
console.log(typeof {
  nombre: "Beto",
  apellido: "Flores"
})

// Nulo null
console.log(typeof null)

// No definido
console.log(typeof undefined)